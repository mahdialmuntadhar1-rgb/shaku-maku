export type TranslationKey = keyof typeof TRANSLATIONS.en;

export function getDirection(language: Language): 'rtl' | 'ltr' {
  return language === 'en' ? 'ltr' : 'rtl';
}

export function normalizeLanguage(value: string | null | undefined): Language {
  return value === 'ar' || value === 'ku' || value === 'en' ? value : 'ar';
}

export function getCategoryName(categoryId: string | null | undefined, language: Language): string {
  const category = CATEGORIES.find((item) => item.id === categoryId);
  return category?.name?.[language] || category?.name?.en || String(categoryId || '');
}

export function getGovernorateName(governorateId: string | null | undefined, language: Language): string {
  const governorate = GOVERNORATES.find((item) => item.code === governorateId);
  return governorate?.name?.[language] || governorate?.name?.en || String(governorateId || '');
}
